class visualizeInfo:
    '''
    class used to collect the parsing information generated by draw function to produce animation
    '''
    def __init__(self, baseImageArray, posDict, dimDict, allPosDict, allDimDict, color_style, edges, arrow_info, name_to_id):
        '''

        Args:
            baseImageArray:
            posDict: position dictionary for the floating species nodes
            dimDict: dimension dictionary for the floating species nodes
            allPosDict: position dictionary for all nodes
            allDimDict: dimension dictionary for all nodes
            color_style: the color_style used to draw the plot
            edges: edges connecting nodes
            arrow_info: information used to draw arrow
            name_to_id: species name to node id dictionary
        '''
        self.baseImageArray = baseImageArray
        self.dimDict = dimDict
        self.posDict = posDict
        self.allPosDict = allPosDict
        self.allDimDict = allDimDict
        self.color_style = color_style
        self.edges = edges
        self.arrow_info = arrow_info
        self.name_to_id = name_to_id

